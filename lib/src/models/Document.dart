class Document {
  final String id;
  final String name;
  final String description;
  final String content;

  const Document({
    this.id,
    this.name,
    this.description,
    this.content
  });

  Document.fromJson(Map<String, dynamic> json)
      : id = json['id'],
        name = json['name'],
        description = json['description'],
        content = json['content'];

}

